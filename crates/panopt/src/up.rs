/// `panopt up` - ensure the daemon, then create or attach this project's
/// cockpit Zellij session.
///
/// `host`, when supplied, is forwarded to `panoptd --host` so the daemon
/// binds to a non-loopback interface (`0.0.0.0` to accept connections from
/// other machines). Ignored if the daemon is already running - the bind
/// address only matters when this call starts a fresh `panoptd`.
pub fn run(plugin: Option<PathBuf>, host: Option<String>, port: u16) -> Result<()> {
    if std::env::var_os("ZELLIJ").is_some() {
        bail!("run `panopt up` from a plain shell, not inside Zellij");
    }

    daemon::ensure(host.as_deref(), port)?;
    mcp::ensure()?;

    let project = std::env::current_dir().context("reading the current directory")?;
    let project = std::fs::canonicalize(&project)
        .with_context(|| format!("no such directory: {}", project.display()))?;
    let session = session_name(&project);

    match session_state(&session) {
        SessionState::Live => {
            println!("attaching to cockpit `{session}`");
            let err = Command::new("zellij").arg("attach").arg(&session).exec();
            return Err(err).context("could not exec `zellij attach`");
        }
        SessionState::Dead => {
            // Quitting a cockpit leaves a dead, resurrectable Zellij session;
            // clear it so `panopt up` boots a fresh one. Daemon state (todos,
            // scratchpads) lives in SQLite and is unaffected.
            println!("clearing the dead cockpit `{session}`");
            let cleared = Command::new("zellij")
                .arg("delete-session")
                .arg(&session)
                .status()
                .context("running `zellij delete-session`")?;
            if !cleared.success() {
                bail!(
                    "could not clear the dead session `{session}`; \
                     remove it with `zellij delete-session {session}`"
                );
            }
        }
        SessionState::Absent => {}
    }

    let wasm = match plugin {
        Some(p) => p,
        None => default_plugin_wasm()
            .context("could not find the panopt-zellij plugin .wasm; pass --plugin <path>")?,
    };
    if let Err(e) = ensure_clipboard_permission_granted(&wasm) {
        eprintln!(
            "warning: could not pre-grant Zellij's `WriteToClipboard` \
             permission for the sidebar plugin ({e:#}); the cockpit will \
             still come up but the form's drag-to-copy may not reach the \
             system clipboard until you approve the permission prompt"
        );
    }
    let copy_helper = match ensure_copy_helper_script() {
        Ok(p) => Some(p),
        Err(e) => {
            eprintln!(
                "warning: could not write the cockpit's copy-to-OSC52 helper \
                 script ({e:#}); the cockpit's form drag-to-copy may not \
                 reach the host terminal's clipboard"
            );
            None
        }
    };
    let layout = render_layout(&project, &wasm, port)?;
    let config = match render_config(copy_helper.as_deref()) {
        Ok(path) => Some(path),
        Err(e) => {
            eprintln!(
                "warning: could not prepare the cockpit's Zellij config ({e:#}); \
                 starting with your default keybindings - Alt-N will open a shell \
                 instead of a blank viewer pane, and scrolling / navigating out \
                 of a locked content pane will require Ctrl-G first"
            );
            None
        }
    };
    println!("starting cockpit `{session}`");
    // `--new-session-with-layout` always creates a fresh session; plain
    // `--layout` alongside `--session` would instead try to add tabs to an
    // existing session of that name. `--config` points Zellij at the generated
    // cockpit config, which retargets pane-close / quit / Alt-N through the
    // sidebar plugin, splices scroll mode and pane navigation into the locked
    // block, and wires the copy_command helper for OSC-52 clipboard egress.
    let mut zellij = Command::new("zellij");
    zellij.arg("--session").arg(&session);
    if let Some(config) = &config {
        zellij.arg("--config").arg(config);
    }
    zellij.arg("--new-session-with-layout").arg(&layout);
    let err = zellij.exec();
    Err(err).context("could not exec `zellij`")
}

/// The cockpit session name for a project: `panopt-<basename>-<hash>`. The
/// basename is reduced to characters Zellij accepts; the hash is a short
/// fingerprint of the canonical project path, so two projects sharing a
/// basename (`~/a/frontend`, `~/b/frontend`) land on distinct sessions
/// instead of one silently attaching to the other's cockpit.
fn session_name(project: &Path) -> String {
    let base = project
        .file_name()
        .and_then(|n| n.to_str())
        .unwrap_or("project");
    let safe: String = base
        .chars()
        .map(|c| if c.is_ascii_alphanumeric() { c } else { '-' })
        .collect();
    let hash = short_path_hash(project);
    format!("panopt-{safe}-{hash}")
}

/// Stable 7-hex-char fingerprint of the canonical project path. Used as the
/// disambiguating suffix on the cockpit session name; see [`session_name`].
fn short_path_hash(project: &Path) -> String {
    use std::hash::{Hash, Hasher};
    let mut hasher = std::collections::hash_map::DefaultHasher::new();
    project.hash(&mut hasher);
    let h = hasher.finish();
    format!("{h:016x}").chars().take(7).collect()
}

/// Whether a Zellij session named `name` exists, and if so whether it is live
/// or dead (exited but still resurrectable).
enum SessionState {
    Absent,
    Live,
    Dead,
}

fn session_state(name: &str) -> SessionState {
    let Ok(out) = Command::new("zellij").arg("list-sessions").output() else {
        return SessionState::Absent;
    };
    let text = strip_ansi(&String::from_utf8_lossy(&out.stdout));
    for line in text.lines() {
        if line.split_whitespace().next() == Some(name) {
            return if line.contains("EXITED") {
                SessionState::Dead
            } else {
                SessionState::Live
            };
        }
    }
    SessionState::Absent
}

/// Drop ANSI escape sequences so `zellij list-sessions` output can be parsed.
fn strip_ansi(s: &str) -> String {
    let mut out = String::with_capacity(s.len());
    let mut chars = s.chars();
    while let Some(c) = chars.next() {
        if c == '\u{1b}' {
            // Skip a CSI sequence up to and including its final letter.
            for n in chars.by_ref() {
                if n.is_ascii_alphabetic() {
                    break;
                }
            }
        } else {
            out.push(c);
        }
    }
    out
}

/// Locate the plugin `.wasm` from a dev build, relative to this binary
/// (`<repo>/target/<profile>/panopt`).
fn default_plugin_wasm() -> Option<PathBuf> {
    let exe = std::env::current_exe().ok()?;
    let repo = exe.ancestors().nth(3)?;
    let wasm = repo.join("crates/panopt-zellij/target/wasm32-wasip1/release/panopt-zellij.wasm");
    wasm.is_file().then_some(wasm)
}

/// Render the cockpit layout for `project`, write it to disk, return its path.
fn render_layout(project: &Path, wasm: &Path, port: u16) -> Result<PathBuf> {
    let panopt_bin = std::env::current_exe().context("locating the panopt binary")?;
    let body = LAYOUT_TEMPLATE
        .replace("{{PROJECT}}", &project.to_string_lossy())
        .replace("{{WASM}}", &wasm.to_string_lossy())
        .replace("{{PANOPT_BIN}}", &panopt_bin.to_string_lossy())
        .replace("{{PORT}}", &port.to_string());
    let path = paths::cockpit_layout()?;
    std::fs::write(&path, body).with_context(|| format!("writing {}", path.display()))?;
    Ok(path)
}

/// Retarget Zellij's Alt-N keybinding to `Run` of `zellij action pipe`, which
/// pipes a message to the sidebar plugin telling it to spawn a blank viewer
/// pane.
fn retarget_new_pane_binding(base: &str) -> (String, bool) {
    let tweaked = base.replace(NEW_PANE_BIND_FROM, NEW_PANE_BIND_TO);
    let changed = tweaked != base;
    (tweaked, changed)
}

/// Write the tiny shell script Zellij's `copy_command` points at, so that
/// the cockpit's copy gesture lands in the host terminal's clipboard.
///
/// Why a script: Zellij parses `copy_command` by splitting on single
/// spaces and `Command::new`ing the first token (see
/// `zellij-server/src/tab/copy_command.rs`), so we can't pass a `sh -c
/// 'printf ...'` because the quoted shell payload would be torn apart
/// by `split(' ')`. Pointing it at a known-path executable sidesteps
/// that.
///
/// Why bypass Zellij's own OSC 52 path: Zellij intercepts OSC 52 emitted
/// by a pane and re-emits it via `ClipboardProvider::Osc52`, which adds
/// the sequence as a pre-VTE instruction on the next render to each
/// connected client. In some chain configurations (WezTerm-over-SSH was
/// the smoking gun) the re-emitted sequence does not actually make it
/// to the host clipboard. Setting `copy_command` skips the pre-VTE path
/// entirely and writes OSC 52 directly to `/dev/tty`, which is the
/// Zellij client's controlling terminal - i.e. the user's WezTerm pty
/// via SSH. That always works because the OSC 52 reaches the terminal
/// emulator unmolested.
fn ensure_copy_helper_script() -> Result<PathBuf> {
    let path = paths::copy_helper_script()?;
    // Body: capture stdin to a temp file, log what we got (size + content)
    // for diagnostic purposes, then base64-encode and write OSC 52 to
    // /dev/tty. The temp-file detour lets us read stdin twice (once for the
    // log, once for base64); without it we'd consume stdin in the encoder
    // and the log line would be blank.
    //
    // Linux `base64 -w 0` suppresses line wrap; macOS `base64` has no `-w`
    // but emits one line by default - the `tr -d '\n'` fallback covers both.
    // The OSC 52 terminator is `\x07` (BEL); some terminals also accept
    // `\x1b\\` (ST) but BEL is universal.
    //
    // The debug log lives at `~/.local/share/panopt/copy-debug.log` and
    // grows without bound; it's a deliberate one-off troubleshooting aid.
    let log_path = paths::copy_debug_log()?;
    let body = format!(
        r#"#!/bin/sh
# Generated by `panopt up`. Reads selection text on stdin and emits OSC
# 52 to the user's terminal so the host clipboard updates. Zellij
# invokes this as its `copy_command`.
#
# Why the /proc fd hunt: Zellij's `copy_command` subprocess is forked
# by the server with stdin piped, no controlling terminal
# (`/dev/tty` ENXIOs), and stdout inherited from the server. The
# server is daemonized so its stdout points at /dev/null - writes
# succeed but go nowhere. The only fd in the system that's actually
# connected to the user's WezTerm-via-SSH pty is the Zellij CLIENT
# process's fd/1; we narrow to zellij processes with `pgrep -x` and
# pick the first whose fd/1 resolves to a /dev/pts/* device.
#
# Why pgrep, not `for p in /proc/[0-9]*; cat $p/comm`: Zellij's
# `copy_command` hard-times-out after 1 second
# (zellij-server/src/tab/copy_command.rs:46) and kills the script. A
# walk over ~500 entries with a fork-per-cat lands right at 1.0s on
# this box, so half of every selection silently dies. `pgrep -x
# zellij` returns in ~50ms.
LOG={log_path}
tmp=$(mktemp) || tmp=/tmp/panopt-copy-$$.tmp
cat > "$tmp"
{{
  echo "=== $(date) pid=$$ tty=$(tty 2>/dev/null) ==="
  echo "stdin bytes: $(wc -c < "$tmp")"
  printf 'stdin head:  '; head -c 200 "$tmp"; echo
}} >> "$LOG" 2>/dev/null
b=$(base64 -w 0 < "$tmp" 2>/dev/null || base64 < "$tmp" | tr -d '\n')
echo "base64:      $b" >> "$LOG" 2>/dev/null
target=""
for pid in $(pgrep -x zellij 2>/dev/null); do
  fd1="/proc/$pid/fd/1"
  link=$(readlink "$fd1" 2>/dev/null) || continue
  case "$link" in
    /dev/pts/*|/dev/tty*)
      target=$fd1
      echo "target pid=$pid fd1=$link" >> "$LOG"
      break
      ;;
  esac
done
if [ -n "$target" ]; then
  printf '\033]52;c;%s\007' "$b" > "$target" 2>>"$LOG"
  echo "osc52 rc: $?" >> "$LOG"
else
  echo "osc52: no zellij client pty found, falling back to /dev/tty" >> "$LOG"
  printf '\033]52;c;%s\007' "$b" > /dev/tty 2>>"$LOG"
  echo "osc52 tty rc: $?" >> "$LOG"
fi
rm -f "$tmp"
"#,
        log_path = log_path.display(),
    );
    std::fs::write(&path, body).with_context(|| format!("writing {}", path.display()))?;
    use std::os::unix::fs::PermissionsExt;
    let mut perms = std::fs::metadata(&path)
        .with_context(|| format!("stat-ing {}", path.display()))?
        .permissions();
    perms.set_mode(0o755);
    std::fs::set_permissions(&path, perms)
        .with_context(|| format!("chmod 0755 {}", path.display()))?;
    Ok(path)
}

/// Append `copy_command "<script_path>"` to the generated cockpit config if
/// the user's base config has no active (uncommented) `copy_command` line.
/// We respect a user-set `copy_command` because they may have it pointing
/// at something deliberate (`pbcopy`, a remote forward, etc.); only when
/// no setting exists do we wire our OSC-52-to-/dev/tty helper in.
fn append_copy_command_if_unset(base: &str, script_path: &Path) -> String {
    let has_setting = base.lines().any(|line| {
        let trimmed = line.trim_start();
        // Skip KDL comments: `// copy_command "..."` does NOT count as set.
        if trimmed.starts_with("//") {
            return false;
        }
        trimmed.starts_with("copy_command")
    });
    if has_setting {
        return base.to_string();
    }
    let mut out = base.to_string();
    if !out.ends_with('\n') {
        out.push('\n');
    }
    out.push_str(&format!("copy_command \"{}\"\n", script_path.display()));
    out
}

/// Pre-grant Zellij's `WriteToClipboard` permission for the sidebar plugin
/// so the auto-locked cockpit can copy form selections without a runtime
/// permission dialog ("can the plugin access your clipboard?") that the
/// user might not see or might dismiss with the wrong button.
///
/// The cache lives at `~/.cache/zellij/permissions.kdl` (Zellij's exact
/// path on Linux; on macOS Zellij actually keeps it elsewhere - we still
/// write to the same `dirs::cache_dir()`-derived path, which is good
/// enough for users who run the cockpit on Linux as Zellij itself
/// reads from the matching location there).
///
/// File format (Zellij's own, parsed by `PermissionCache::from_string`):
///
/// ```kdl
/// "<absolute wasm path>" {
///     ChangeApplicationState
///     RunCommands
///     ReadApplicationState
///     WriteToClipboard
/// }
/// ```
///
/// If the user already approved the other three (the run dialog has been
/// dismissed at least once), only `WriteToClipboard` is added inside the
/// existing block. If the entry doesn't exist yet a fresh block is
/// appended. Either way the file is created if missing.
fn ensure_clipboard_permission_granted(wasm: &Path) -> Result<()> {
    let cache_dir = dirs::cache_dir().context("locating the user cache directory")?;
    let cache_file = cache_dir.join("zellij").join("permissions.kdl");
    let wasm_str = wasm.to_string_lossy().to_string();

    let existing = match std::fs::read_to_string(&cache_file) {
        Ok(s) => s,
        Err(e) if e.kind() == std::io::ErrorKind::NotFound => String::new(),
        Err(e) => {
            return Err(e).with_context(|| format!("reading {}", cache_file.display()));
        }
    };

    let Some(new_content) = grant_clipboard_in_cache(&existing, &wasm_str) else {
        // Already present; nothing to do.
        return Ok(());
    };

    if let Some(parent) = cache_file.parent() {
        std::fs::create_dir_all(parent)
            .with_context(|| format!("creating {}", parent.display()))?;
    }
    std::fs::write(&cache_file, new_content)
        .with_context(|| format!("writing {}", cache_file.display()))?;
    Ok(())
}

/// Pure transform: given the current `permissions.kdl` contents and our
/// plugin's wasm path, return the updated contents if a write is needed
/// (i.e. our plugin's entry was missing or did not list
/// `WriteToClipboard`), or `None` if no change.
///
/// Hand-rolled string surgery rather than a KDL parser dep. Format is
/// regular enough that finding `"<path>" {` plus brace balancing covers
/// every shape Zellij writes itself.
fn grant_clipboard_in_cache(existing: &str, wasm_path: &str) -> Option<String> {
    const GRANT: &str = "WriteToClipboard";
    let key = format!("\"{wasm_path}\"");
    if let Some(start) = existing.find(&key) {
        // Found a block for our plugin; splice in WriteToClipboard if
        // missing, else no-op.
        let rel_open = existing[start..].find('{')?;
        let open = start + rel_open;
        let mut depth: i32 = 0;
        let mut close: Option<usize> = None;
        for (i, b) in existing.as_bytes().iter().enumerate().skip(open) {
            match b {
                b'{' => depth += 1,
                b'}' => {
                    depth -= 1;
                    if depth == 0 {
                        close = Some(i);
                        break;
                    }
                }
                _ => {}
            }
        }
        let close = close?;
        let body = &existing[open + 1..close];
        if body.contains(GRANT) {
            return None;
        }
        let mut out = String::with_capacity(existing.len() + GRANT.len() + 8);
        out.push_str(&existing[..close]);
        out.push_str("    ");
        out.push_str(GRANT);
        out.push('\n');
        out.push_str(&existing[close..]);
        return Some(out);
    }
    // No block for this plugin yet; append a fresh one with the four
    // permissions the plugin actually requests today.
    let mut out = String::from(existing);
    if !out.is_empty() && !out.ends_with('\n') {
        out.push('\n');
    }
    out.push_str(&format!(
        "\"{wasm_path}\" {{\n    \
            ReadApplicationState\n    \
            ChangeApplicationState\n    \
            RunCommands\n    \
            WriteToClipboard\n\
         }}\n"
    ));
    Some(out)
}

/// Inject extra `locked`-mode keybinds into the base config's first
/// `keybinds` block so the cockpit-essential keys keep working when the
/// plugin auto-locks a content pane. Zellij's stock dump puts these in
/// `shared_except "locked"`, which strips them under Locked mode - without
/// this injection a user whose focused viewer / agent pane has just been
/// auto-locked cannot enter scroll mode for pane-bounded selection (Ctrl-S),
/// move to another pane (Alt-arrows), spawn a new one (the cockpit's
/// rewritten Alt-N), or quit (the rewritten Ctrl-Q) without first pressing
/// Ctrl-G to escape - and the plugin would immediately re-lock them on the
/// next PaneUpdate.
///
/// Appending a *second* `keybinds { ... }` block does not work: Zellij only
/// reads the first top-level `keybinds` node and silently ignores the rest
/// (see the `TODO` at `zellij-utils kdl/mod.rs:4862`). So we walk the base
/// config, find the first `locked {` header, balance braces to its matching
/// close, and splice our binds in just before it. The bool reports whether
/// the splice succeeded - a config without a `locked` block (e.g. fully
/// custom and unconventional) gets a warning so the user can wire it by
/// hand.
fn inject_locked_keybinds(base: &str) -> (String, bool) {
    let key = "locked {";
    let Some(start) = base.find(key) else {
        return (base.to_string(), false);
    };
    // Byte index of the `{` itself - balance from here.
    let open = start + key.len() - 1;
    let bytes = base.as_bytes();
    let mut depth: i32 = 0;
    let mut close = None;
    for (i, b) in bytes.iter().enumerate().skip(open) {
        match b {
            b'{' => depth += 1,
            b'}' => {
                depth -= 1;
                if depth == 0 {
                    close = Some(i);
                    break;
                }
            }
            _ => {}
        }
    }
    let Some(close) = close else {
        return (base.to_string(), false);
    };
    let extras = locked_extra_binds();
    let mut out = String::with_capacity(base.len() + extras.len());
    out.push_str(&base[..close]);
    out.push_str(&extras);
    out.push_str(&base[close..]);
    (out, true)
}

/// The body of bindings injected into the existing locked block by
/// [`inject_locked_keybinds`]. Indentation matches the typical eight-space
/// inner indent of a Zellij `mode { bind ... }` body so the spliced lines
/// sit visually alongside whatever was already there. The Alt-N and Ctrl-Q
/// binds reuse the cockpit's pipe-gated rewrites so spawn and quit still go
/// through the sidebar gatekeeper. Ctrl-S mirrors Zellij's stock scroll-mode
/// entry binding so a one-keystroke pane-bounded selection / scrollback path
/// is available even while a content pane is auto-locked.
///
/// PageUp / PageDown go here (rather than a top-level `shared { ... }`
/// block) so they only fire on the auto-locked content pane - directly
/// binding to `PageScrollUp` / `PageScrollDown` keeps the gesture
/// transparent (Zellij scrolls the pane's scrollback and auto-returns on
/// next I/O) without intercepting the keys on the sidebar plugin panes,
/// which stay in Normal mode and whose own `handle_key` page-steps the
/// list cursor.
fn locked_extra_binds() -> String {
    let focus_binds = focus_pane_binds();
    format!(
        "        bind \"Ctrl s\" {{ SwitchToMode \"scroll\"; }}\n        \
         bind \"PageUp\" {{ PageScrollUp; }}\n        \
         bind \"PageDown\" {{ PageScrollDown; }}\n        \
         bind \"Alt Left\" {{ MoveFocusOrTab \"Left\"; }}\n        \
         bind \"Alt Right\" {{ MoveFocusOrTab \"Right\"; }}\n        \
         bind \"Alt Up\" {{ MoveFocus \"Up\"; }}\n        \
         bind \"Alt Down\" {{ MoveFocus \"Down\"; }}\n        \
         {NEW_PANE_BIND_TO}\n        \
         {QUIT_BIND_TO}\n        \
         {SEARCH_BIND_TO}\n        \
         {focus_binds}\n    "
    )
}

/// Force `mouse_mode true` in the generated config. Replaces an explicit
/// `mouse_mode false` if the user's base config has one; otherwise appends
/// the line iff `mouse_mode true` is not already present. Either way, the
/// generated cockpit config carries an explicit on, so the auto-locked mode
/// flip never lands users in a state where mouse events stop reaching their
/// content panes.
fn ensure_mouse_mode_enabled(base: &str) -> String {
    let flipped = base.replace(MOUSE_MODE_OFF, MOUSE_MODE_ON);
    if flipped.contains(MOUSE_MODE_ON) {
        return flipped;
    }
    let mut out = flipped;
    if !out.ends_with('\n') {
        out.push('\n');
    }
    out.push_str(MOUSE_MODE_ON);
    out.push('\n');
    out
}

/// Force pane frames on. The cockpit signals focus through the focused pane's
/// frame color (see [`apply_focus_theme`]), which is invisible with frames off.
/// Zellij's stock default is already on, so this only rewrites an explicit
/// `pane_frames false`; an unset config keeps the on-by-default behavior.
fn ensure_pane_frames_enabled(base: &str) -> String {
    base.replace(PANE_FRAMES_OFF, PANE_FRAMES_ON)
}

/// Make the focused pane unmistakable by defining and activating the cockpit's
/// `panopt` frame theme - unless the user has chosen their own theme, in which
/// case we leave it alone (activating ours would swap every non-frame color
/// back to Zellij's default, since our theme is intentionally partial). When
/// skipped, the returned `Option` carries the user's theme name so the caller
/// can explain the gap.
///
/// When applying: the `panopt` theme body is spliced into an existing
/// top-level `themes { ... }` block if the base config has one (Zellij reads
/// only the *first* such block, so a second appended block would be silently
/// ignored - the same gotcha as keybinds), otherwise a fresh `themes` block is
/// appended. Either way a `theme "panopt"` directive is appended to activate it.
fn apply_focus_theme(base: &str) -> (String, Option<String>) {
    if let Some(name) = active_theme_name(base) {
        return (base.to_string(), Some(name));
    }
    let with_def = match splice_into_themes_block(base, FOCUS_THEME_DEF) {
        Some(spliced) => spliced,
        None => {
            let mut out = String::from(base);
            if !out.is_empty() && !out.ends_with('\n') {
                out.push('\n');
            }
            out.push_str("themes {\n");
            out.push_str(FOCUS_THEME_DEF);
            out.push_str("}\n");
            out
        }
    };
    let mut out = with_def;
    if !out.ends_with('\n') {
        out.push('\n');
    }
    out.push_str(&format!("theme \"{FOCUS_THEME_NAME}\"\n"));
    (out, None)
}

/// The name in an *active* (uncommented) top-level `theme "<name>"` directive,
/// or `None` when the base config leaves the theme at Zellij's default - the
/// stock dump keeps that line commented. The trailing space in the `theme `
/// prefix keeps `theme_dir` from matching.
fn active_theme_name(base: &str) -> Option<String> {
    base.lines().find_map(|line| {
        let line = line.trim_start();
        if line.starts_with("//") {
            return None;
        }
        let rest = line.strip_prefix("theme ")?.trim_start();
        let inner = rest.strip_prefix('"')?;
        let end = inner.find('"')?;
        Some(inner[..end].to_string())
    })
}

/// Splice `def` (an indented `name { ... }` theme body) in just before the
/// closing brace of the first top-level `themes {` block, returning the new
/// config - or `None` when the base config has no such block. Walks from the
/// block's opening brace and balances braces to find its matching close, the
/// same hand-rolled approach [`inject_locked_keybinds`] uses (no KDL-parser
/// dep). Only the *first* `themes` block is honored by Zellij, so that is the
/// one we extend.
fn splice_into_themes_block(base: &str, def: &str) -> Option<String> {
    let key = "themes {";
    let start = base.find(key)?;
    // Skip a commented occurrence (`// themes {`): require the line to start
    // with `themes` after trimming, otherwise keep looking is overkill - the
    // dumped config never comments a `themes {` header, and a user one is
    // uncommented. Guard the obvious comment case all the same.
    let line_start = base[..start].rfind('\n').map_or(0, |i| i + 1);
    if base[line_start..start].trim_start().starts_with("//") {
        return None;
    }
    let open = start + key.len() - 1;
    let mut depth: i32 = 0;
    let mut close = None;
    for (i, b) in base.as_bytes().iter().enumerate().skip(open) {
        match b {
            b'{' => depth += 1,
            b'}' => {
                depth -= 1;
                if depth == 0 {
                    close = Some(i);
                    break;
                }
            }
            _ => {}
        }
    }
    let close = close?;
    let mut out = String::with_capacity(base.len() + def.len());
    out.push_str(&base[..close]);
    out.push_str(def);
    out.push_str(&base[close..]);
    Some(out)
}

/// Retarget every CloseFocus/CloseTab/Quit keybind to `Run` of
/// `zellij action pipe`, sending the request to the sidebar plugin instead of
/// acting immediately. The plugin gates these against active items and either
/// refuses (showing a floating dialog) or force-closes via the plugin API,
/// which bypasses the rewritten keybinds.
///
/// The returned tuple reports whether each rewrite was found - users with a
/// reformatted or heavily-customized config may have moved them; we warn so
/// the user knows the gate has gaps. The default Zellij dump emits the
/// CloseFocus binding in multiple modes; `str::replace` catches every copy.
fn retarget_close_bindings(base: &str) -> (String, bool, bool, bool) {
    let after_close_focus = base.replace(CLOSE_FOCUS_BIND_FROM, CLOSE_FOCUS_BIND_TO);
    let close_focus_changed = after_close_focus != base;
    let after_close_tab = after_close_focus.replace(CLOSE_TAB_BIND_FROM, CLOSE_TAB_BIND_TO);
    let close_tab_changed = after_close_tab != after_close_focus;
    let after_quit = after_close_tab.replace(QUIT_BIND_FROM, QUIT_BIND_TO);
    let quit_changed = after_quit != after_close_tab;
    (
        after_quit,
        close_focus_changed,
        close_tab_changed,
        quit_changed,
    )
}

/// Locate the Zellij config file Zellij itself would load, following its own
/// lookup order: `$ZELLIJ_CONFIG_FILE`, `$ZELLIJ_CONFIG_DIR/config.kdl`,
/// `$XDG_CONFIG_HOME/zellij/config.kdl`, then `~/.config/zellij/config.kdl`.
/// `None` when the user keeps no config and relies on Zellij's defaults.
fn user_zellij_config() -> Option<PathBuf> {
    if let Some(file) = std::env::var_os("ZELLIJ_CONFIG_FILE") {
        let path = PathBuf::from(file);
        if path.is_file() {
            return Some(path);
        }
    }
    let config_kdl = |dir: PathBuf| {
        let path = dir.join("config.kdl");
        path.is_file().then_some(path)
    };
    if let Some(dir) = std::env::var_os("ZELLIJ_CONFIG_DIR") {
        if let Some(path) = config_kdl(PathBuf::from(dir)) {
            return Some(path);
        }
    }
    if let Some(dir) = std::env::var_os("XDG_CONFIG_HOME") {
        if let Some(path) = config_kdl(PathBuf::from(dir).join("zellij")) {
            return Some(path);
        }
    }
    dirs::home_dir().and_then(|home| config_kdl(home.join(".config").join("zellij")))
}

/// The Zellij config to derive the cockpit's from: the user's own config, or -
/// when they keep none - Zellij's built-in defaults via `zellij setup
/// --dump-config`.
fn base_zellij_config() -> Result<String> {
    if let Some(path) = user_zellij_config() {
        return std::fs::read_to_string(&path)
            .with_context(|| format!("reading the Zellij config {}", path.display()));
    }
    let out = Command::new("zellij")
        .args(["setup", "--dump-config"])
        .output()
        .context("running `zellij setup --dump-config`")?;
    if !out.status.success() {
        bail!("`zellij setup --dump-config` exited with a failure");
    }
    String::from_utf8(out.stdout).context("`zellij setup --dump-config` produced invalid UTF-8")
}

/// Generate the cockpit's Zellij config - the user's config with Alt-N
/// retargeted to spawn a blank viewer pane via the sidebar plugin, the
/// close/quit binds gated through the sidebar, mouse mode forced on, and
/// scroll mode + pane navigation spliced into the locked block - write it to
/// disk, and return its path. Regenerated on each run, so it tracks the
/// user's own config.
fn render_config(copy_helper: Option<&Path>) -> Result<PathBuf> {
    let base = base_zellij_config()?;
    let (tweaked, new_pane_changed) = retarget_new_pane_binding(&base);
    if !new_pane_changed {
        eprintln!(
            "warning: Zellij's `Alt n` `NewPane` binding was not found in the \
             expected form, so it could not be remapped; Alt-N will open a \
             shell rather than a blank viewer pane"
        );
    }
    let (tweaked, close_focus_changed, close_tab_changed, quit_changed) =
        retarget_close_bindings(&tweaked);
    if !close_focus_changed {
        eprintln!(
            "warning: Zellij's `CloseFocus` binding was not found in the \
             expected form, so it could not be gated; closing a pane will \
             bypass the active-item guard"
        );
    }
    if !close_tab_changed {
        eprintln!(
            "warning: Zellij's `CloseTab` binding was not found in the \
             expected form, so it could not be gated; closing a tab will \
             bypass the active-item guard"
        );
    }
    if !quit_changed {
        eprintln!(
            "warning: Zellij's `Ctrl q` `Quit` binding was not found in the \
             expected form, so it could not be gated; quitting will bypass \
             the active-item guard"
        );
    }
    let tweaked = ensure_mouse_mode_enabled(&tweaked);
    let (tweaked, locked_injected) = inject_locked_keybinds(&tweaked);
    if !locked_injected {
        eprintln!(
            "warning: no `locked {{ ... }}` block was found in your Zellij \
             config, so the cockpit could not inject Alt-arrow navigation \
             and the rewritten Alt-N / Ctrl-Q binds into Locked mode; while \
             focused on a content pane (which the sidebar auto-locks), those \
             keys will be inert and you will have to press Ctrl-G to escape \
             before moving focus or quitting"
        );
    }
    let tweaked = match copy_helper {
        Some(path) => append_copy_command_if_unset(&tweaked, path),
        None => tweaked,
    };
    let tweaked = ensure_pane_frames_enabled(&tweaked);
    let (tweaked, user_theme) = apply_focus_theme(&tweaked);
    if let Some(name) = user_theme {
        eprintln!(
            "warning: your Zellij config sets `theme \"{name}\"`, so the cockpit \
             left it untouched and did not apply its focus-emphasis frame \
             colors (activating the cockpit theme would override that theme's \
             other colors); to brighten the focused-pane border, copy the \
             `frame_selected` / `frame_unselected` colors from panopt's \
             `panopt` theme into \"{name}\""
        );
    }
    let body = format!(
        "// PANopt cockpit Zellij config - generated by `panopt up`, regenerated\n\
         // each run from your own Zellij config. Alt-N is retargeted to pipe a\n\
         // `panopt:spawn-blank-pane` message to the sidebar plugin, which spawns\n\
         // a blank viewer pane with its own routing slot. CloseFocus, CloseTab,\n\
         // and Quit are retargeted to pipe close-request messages to the sidebar,\n\
         // which gates them against active agents/commands/terminals. `mouse_mode\n\
         // true` is forced so the auto-locked input mode (set by the plugin when\n\
         // focus lands on a content pane) doesn't strand the user without mouse\n\
         // events reaching the viewer / terminal panes. Alt-arrow pane\n\
         // navigation, Ctrl-S (scroll mode), PageUp / PageDown (direct\n\
         // PageScrollUp / PageScrollDown), and the rewritten Alt-N and Ctrl-Q\n\
         // are spliced into the existing `locked` block (a second top-level\n\
         // `keybinds` block does not work - Zellij only reads the first one),\n\
         // so the user can scroll, select, page through scrollback, leave the\n\
         // pane, spawn a new one, or quit without a Ctrl-G escape that would\n\
         // just be re-locked on the next PaneUpdate. PageUp / PageDown live in\n\
         // the locked block rather than a top-level `shared {{ ... }}` so they\n\
         // only intercept on auto-locked content panes; sidebar plugin panes\n\
         // stay in Normal mode and receive the keys directly, where the\n\
         // plugin's own `handle_key` page-steps the list cursor. Finally a\n\
         // `panopt` theme is appended and activated (unless you set your own\n\
         // theme): it overrides only the frame colors so the focused pane's\n\
         // border is loud and unfocused ones recede, with `pane_frames` forced\n\
         // on so that cue is visible.\n\
         {tweaked}"
    );
    let path = paths::cockpit_config()?;
    std::fs::write(&path, body).with_context(|| format!("writing {}", path.display()))?;
    Ok(path)
}

