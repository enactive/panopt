    #[test]
    fn retarget_close_bindings_funnels_all_three_actions_through_pipes() {
        // A fragment that mirrors the default Zellij dump: CloseFocus appears
        // in two modes (pane and search) - both must be rewritten - and
        // CloseTab and Quit appear once each.
        let base = "\
            pane {\n        \
                bind \"x\" { CloseFocus; SwitchToMode \"Normal\"; }\n    \
            }\n    \
            tab {\n        \
                bind \"x\" { CloseTab; SwitchToMode \"Normal\"; }\n    \
            }\n    \
            search {\n        \
                bind \"x\" { CloseFocus; SwitchToMode \"Normal\"; }\n    \
            }\n    \
            shared_except \"locked\" {\n        \
                bind \"Ctrl q\" { Quit; }\n    \
            }\n";
        let (tweaked, close_focus, close_tab, quit) = retarget_close_bindings(base);
        assert!(close_focus, "CloseFocus binding must be rewritten");
        assert!(close_tab, "CloseTab binding must be rewritten");
        assert!(quit, "Quit binding must be rewritten");
        // Every destructive action goes through the same pipe pattern so the
        // Todos plugin pane is the single gatekeeper.
        assert!(tweaked.contains("\"panopt:close-focus-request\""));
        assert!(tweaked.contains("\"panopt:close-tab-request\""));
        assert!(tweaked.contains("\"panopt:quit-request\""));
        // Both CloseFocus occurrences (pane and search modes) are caught.
        assert_eq!(tweaked.matches("\"panopt:close-focus-request\"").count(), 2);
        // Every gated pipe carries the mode=todos narrowing so it reaches
        // only the gatekeeper and not the other four plugin panes. Four
        // occurrences in this fixture: two CloseFocus + one CloseTab + one
        // Quit (the floating dialog's reply is sent from `close_gate.rs`,
        // not this rewrite path).
        assert_eq!(
            tweaked
                .matches("\"--plugin-configuration\" \"mode=todos\"")
                .count(),
            4
        );
        // No raw destructive action remains: every path is gated.
        assert!(!tweaked.contains("CloseFocus"));
        assert!(!tweaked.contains("CloseTab"));
        assert!(!tweaked.contains("{ Quit; }"));
        // The mode return is preserved so the user still leaves pane/tab mode
        // after the pipe is sent - matching the original UX shape.
        assert!(tweaked.contains("SwitchToMode \"Normal\""));
        // Transient pipe panes close themselves the moment the CLI exits.
        assert!(tweaked.contains("close_on_exit true"));
    }

