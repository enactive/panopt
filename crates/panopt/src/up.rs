/// The cockpit layout: five fixed plugin panes stacked vertically on the
/// left (one per resource kind) and a single content pane on the right.
///
/// Each plugin pane is one Zellij plugin instance running the same wasm
/// binary, configured by a distinct `mode` value (`todos`, `agents`,
/// `terminals`, `commands`, `scratchpads`). Zellij keys plugin identity on
/// `(URL, configuration)`, so the five panes are five distinct instances.
/// Layout-pinned in the `ui` tab template, so adding or removing panes on
/// the right cannot reshape them.
///
/// The content pane starts as one empty `panopt _viewer`; selections in any
/// of the five panes swap their target into that one slot, suppressing
/// whatever was there. The user is free to split the content pane to make
/// further slots.
///
/// `{{PROJECT}}`, `{{WASM}}`, `{{PANOPT_BIN}}`, and `{{PORT}}` are
/// substituted by [`render_layout`]; each may appear more than once.
const LAYOUT_TEMPLATE: &str = r#"// PANopt cockpit - generated by `panopt up`. Regenerated on each run.
// Five plugin panes stack vertically on the LEFT (~25% column), one per
// resource kind; a single content pane sits on the RIGHT. Selections swap
// into that one pane - suppressed panes keep running, hidden.
layout {
    cwd "{{PROJECT}}"

    // The fixed shell of every tab: five plugin panes pinned left (one per
    // mode), the content pane on the right flows into the `children` slot.
    tab_template name="ui" {
        pane split_direction="vertical" {
            pane size="25%" split_direction="horizontal" {
                pane size="30%" {
                    plugin location="file:{{WASM}}" {
                        mode "todos"
                        ws "{{PROJECT}}"
                        panopt_bin "{{PANOPT_BIN}}"
                        port "{{PORT}}"
                    }
                }
                pane size="15%" {
                    plugin location="file:{{WASM}}" {
                        mode "agents"
                        ws "{{PROJECT}}"
                        panopt_bin "{{PANOPT_BIN}}"
                        port "{{PORT}}"
                    }
                }
                pane size="15%" {
                    plugin location="file:{{WASM}}" {
                        mode "terminals"
                        ws "{{PROJECT}}"
                        panopt_bin "{{PANOPT_BIN}}"
                        port "{{PORT}}"
                    }
                }
                pane size="15%" {
                    plugin location="file:{{WASM}}" {
                        mode "commands"
                        ws "{{PROJECT}}"
                        panopt_bin "{{PANOPT_BIN}}"
                        port "{{PORT}}"
                    }
                }
                pane size="25%" {
                    plugin location="file:{{WASM}}" {
                        mode "scratchpads"
                        ws "{{PROJECT}}"
                        panopt_bin "{{PANOPT_BIN}}"
                        port "{{PORT}}"
                    }
                }
            }
            pane {
                children
            }
        }
    }

    // Initial state: the five plugin panes plus one empty document viewer.
    ui {
        pane command="{{PANOPT_BIN}}" {
            args "_viewer" "--slot" "main" "--port" "{{PORT}}"
        }
    }
}
"#;

/// Zellij's stock Alt-N binding opens a shell pane. The cockpit retargets it
/// to send the `panopt:spawn-blank-pane` pipe to the Todos plugin pane,
/// which (as cockpit gatekeeper) spawns a fresh empty-viewer pane with its
/// own routing slot - so any of the five plugin panes can later re-point it
/// on a selection.
///
/// Why `Run "zellij" "action" "pipe" ...` instead of `MessagePlugin`: Zellij's
/// KDL parser folds `MessagePlugin`'s metadata fields (`name`, `payload`,
/// `cwd`, `launch_new`, ...) into the plugin's user-configuration map. The
/// runtime then keys plugin identity on `(URL, configuration)`, so a
/// `MessagePlugin` keybind whose configuration has any of those metadata
/// keys can never match a layout plugin whose configuration does not - Zellij
/// launches a *second* plugin instance instead. `zellij action pipe` goes
/// through the running session's IPC bus and reaches the existing plugin
/// without that mismatch. The cost is a brief transient pane while the CLI
/// runs; `close_on_exit true` closes it the moment the pipe is sent.
///
/// Why `--plugin-configuration "mode=todos"`: with five plugin instances
/// (one per mode), an unfiltered pipe broadcasts to all five. Narrowing to
/// `mode=todos` delivers the message only to the gatekeeper. The plugin
/// also early-returns from `pipe()` on non-Todos panes - belt-and-braces if
/// the filter ever fails to match.
const NEW_PANE_BIND_FROM: &str = r#"bind "Alt n" { NewPane; }"#;
const NEW_PANE_BIND_TO: &str = r#"bind "Alt n" { Run "zellij" "action" "pipe" "--name" "panopt:spawn-blank-pane" "--plugin-configuration" "mode=todos" { close_on_exit true; }; }"#;

/// `Alt-/` opens the cockpit's popup search across todos and scratchpads. Bare
/// `/` would intercept every search keystroke in vim, less, shells, etc. on
/// locked content panes, so the binding wears the Alt modifier to stay clear
/// of the inner program's namespace. There is no stock Zellij binding to
/// retarget, so this is appended into the locked block by
/// [`locked_extra_binds`]. The pipe is delivered to the Todos sidebar plugin,
/// which spawns the floating `panopt search` TUI. On sidebar plugin panes
/// (Normal mode), `Alt-/` is handled inside the plugin's own `handle_key`
/// rather than by Zellij - so the gesture works everywhere in the cockpit.
const SEARCH_BIND_TO: &str = r#"bind "Alt /" { Run "zellij" "action" "pipe" "--name" "panopt:open-search" "--plugin-configuration" "mode=todos" { close_on_exit true; }; }"#;

/// `Alt-<1..5>` focuses a sidebar pane, lazygit-style (todo #110):
/// `1`=Todos, `2`=Agents, `3`=Terminals, `4`=Commands, `5`=Scratchpads. These
/// fire on auto-locked content panes; the same gesture on a sidebar plugin
/// pane (Normal mode) is handled inside the plugin's `handle_key`. Both
/// broadcast a `panopt:focus-pane` pipe carrying the target mode's slug as
/// payload; every sidebar instance receives it but only the one whose own
/// slug matches focuses its pane - and that one no-ops while a floating popup
/// (search / gate dialog) holds focus, so the gesture never yanks the user out
/// of a popup. We broadcast (the payload rides as the trailing `-- <slug>`
/// positional that `zellij action pipe` reads) rather than narrow with
/// `--plugin-configuration "mode=<slug>"` because the narrowing routed every
/// digit to the gatekeeper instead of the named instance; the same
/// self-filtering broadcast backs the search pipes. Built rather than
/// `const`-ed because the slug varies per digit; see [`NEW_PANE_BIND_FROM`]
/// for why the cockpit pipes through `zellij action pipe` rather than
/// `MessagePlugin`.
fn focus_pane_binds() -> String {
    [
        (1, "todos"),
        (2, "agents"),
        (3, "terminals"),
        (4, "commands"),
        (5, "scratchpads"),
    ]
    .iter()
    .map(|(digit, mode)| {
        format!(
            r#"bind "Alt {digit}" {{ Run "zellij" "action" "pipe" "--name" "panopt:focus-pane" "--" "{mode}" {{ close_on_exit true; }}; }}"#
        )
    })
    .collect::<Vec<_>>()
    .join("\n        ")
}

